class Tivi{
    constructor(_channel,_status,_volume) {
        this.channel = _channel
        this.status = _status
        this.volume = _volume
    }
    setChannel(newChannel){
        this.channel = newChannel
    }
    setVolume(newVolume){
        this.volume = newVolume
    }
    turnOnOff(){
        this.status != this.status
    }
}
